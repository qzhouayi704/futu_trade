export function Sidebar() {
  const pathname = usePathname();

  return (
    <aside className="w-64 bg-gray-900 text-white min-h-screen flex flex-col">
      {/* Logo */}
      <div className="p-4 border-b border-gray-800">
        <Link href="/" className="flex items-center space-x-2">
          <svg className="w-8 h-8 text-blue-500" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 7h8m0 0v8m0-8l-8 8-4-4-6 6" />
          </svg>
          <span className="text-xl font-bold">富途量化</span>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4">
        <ul className="space-y-2">
          {navItems.map((item) => {
            // 路径匹配：精确匹配或前缀匹配，但排除有更精确子路径匹配的情况
            const hasMoreSpecificMatch = navItems.some(
              (other) => other.path !== item.path
                && other.path.startsWith(item.path + "/")
                && (pathname === other.path || pathname.startsWith(other.path + "/"))
            );
            const isActive = item.path === "/"
              ? pathname === "/"
              : !hasMoreSpecificMatch && (pathname === item.path || pathname.startsWith(item.path + "/"));
            return (
              <li key={item.path}>
                <Link
                  href={item.path}
                  className={`flex items-center space-x-3 px-4 py-3 rounded-lg transition-colors ${
                    isActive
                      ? "bg-blue-600 text-white"
                      : "text-gray-300 hover:bg-gray-800 hover:text-white"
                  }`}
                >
                  {item.icon}
                  <span>{item.name}</span>
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-gray-800 text-sm text-gray-500">
        <p>Next.js 15.4 + React 19</p>
      </div>
    </aside>
  );
}
